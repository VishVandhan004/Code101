import express from "express";
import bodyParser from "body-parser";

const app = express();
const port = 3000;
const masterKey = "4VGP2DN-6EWM4SJ-N6FGRHV-Z3PR3TT";

app.use(bodyParser.urlencoded({ extended: true }));

//Get a random joke
// app.get is an express syntax.., random is the endpoint , req,res is the callback.., all the jokes are in an array of 100 elements and we generate any random joke and round it upto 2 digits using floor method, res.json is response in the format of json..
app.get("/random", (req, res) => {
  const randomIndex = Math.floor(Math.random() * jokes.length);
  res.json(jokes[randomIndex]);
});

//Get a specific joke
// the endpoint must include a pathparamter as well to specify the id.., we get it in a string, so we use a parseInt.., the id which we presented will be checked in the jokes.id array using find() method, if they match, the joke will be displayed..
app.get("/jokes/:id", (req, res) => {
  const id = parseInt(req.params.id);
  const foundJoke = jokes.find((joke) => joke.id === id);
  res.json(foundJoke);
});

//Filter jokes by type
// we use a query parameter in this case.
app.get("/filter", (req, res) => {
  const type = req.query.type; // query params
  const filteredActivities = jokes.filter((joke) => joke.jokeType === type); // we check if the type of the joke is matching with the array elements or not and it displays all the relevant elements related to the query..
  res.json(filteredActivities); // res in form of json.
});

// Post a new joke
// in the jokes endpoint, we get the callback function to handle the request.
app.post("/jokes", (req, res) => {
  // below is the object.
  const newJoke = {
    id: jokes.length + 1, // id is the total length of array + 1 (100+1, 101+1, and so on..)
    jokeText: req.body.text, // we request the text using the body parser.
    jokeType: req.body.type, // we request the type of the joke using body parser..
  };
  // push the new joke to the array
  jokes.push(newJoke);
  console.log(jokes.slice(-1)); // access the last joke..
  res.json(newJoke);
});

//Put a joke
// we use the path param here. after id endpoint and callback, we get the id using parseInt.
app.put("/jokes/:id", (req, res) => {
  const id = parseInt(req.params.id); // id using parseint by using req.params.id
  const replacementJoke = {
    // this is object..
    id: id, // it has id in object
    jokeText: req.body.text, // we request the body text using body parser
    jokeType: req.body.type, // we request the body type using body parser
  };
  // we provide an id, so the below code checks and finds the id in the joke.id array. we get the index of it using the findindex method.
  const searchIndex = jokes.findIndex((joke) => joke.id === id);
// the index will be used in the jokes array to replace the existing joke..
  jokes[searchIndex] = replacementJoke;
  // console.log(jokes);
  res.json(replacementJoke);
});

//Patch a joke
// everything is same as above but we update only one param..
app.patch("/jokes/:id", (req, res) => {
  const id = parseInt(req.params.id);// id using parseint by using req.params.id
  const existingJoke = jokes.find((joke) => joke.id === id); // by using the id we need to check through the whole array..
  const replacementJoke = { // object
    id: id,
    jokeText: req.body.text || existingJoke.jokeText, // it checks if there is anything in the req.body.text or it grabs the text from the existing joke and place it in joketext.
    jokeType: req.body.type || existingJoke.jokeType, // same as above but for joketype..
  };
  const searchIndex = jokes.findIndex((joke) => joke.id === id);
  jokes[searchIndex] = replacementJoke;
  console.log(jokes[searchIndex]);
  res.json(replacementJoke);
});

//DELETE Specific joke
// after the id endpoint and the callback, we get the id using hte req.params.id and parse it as the int and we search through the whole array using the id.. and find the index of the array as well.
app.delete("/jokes/:id", (req, res) => {
  const id = parseInt(req.params.id);
  const searchIndex = jokes.findIndex((joke) => joke.id === id);
  if (searchIndex > -1) { // if the search index >-1, use the splice method to remove it from the array.
    jokes.splice(searchIndex, 1);
    res.sendStatus(200);
  } else {
    res
      .status(404)
      .json({ error: `Joke with id: ${id} not found. No jokes were deleted.` });
  }
});

